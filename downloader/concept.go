// FetchStockConcepts 根据行业和经营范围生成概念与风口
func FetchStockConcepts(market, code string, changePercent float64) (*StockConcepts, error) {
	csCode := "SH" + code
	if strings.ToUpper(market) != "SH" {
		csCode = "SZ" + code
	}
	url := fmt.Sprintf("https://emweb.securities.eastmoney.com/PC_HSF10/CompanySurvey/CompanySurveyAjax?code=%s", csCode)
	body, err := httpGetWithReferer(url, "https://emweb.securities.eastmoney.com/")
	if err != nil {
		return nil, err
	}
	var resp struct {
		Jbzl struct {
			Sshy   string `json:"sshy"`
			Jyfw   string `json:"jyfw"`
			Sszjhy string `json:"sszjhhy"`
		} `json:"jbzl"`
	}
	if err := json.Unmarshal(body, &resp); err != nil {
		return nil, err
	}

	concepts := make([]string, 0)
	seen := make(map[string]struct{})

	// 1. 行业映射（优先级最高）
	if resp.Jbzl.Sshy != "" {
		if list, ok := industryConceptMap[resp.Jbzl.Sshy]; ok {
			for _, c := range list {
				if _, exists := seen[c]; !exists {
					seen[c] = struct{}{}
					concepts = append(concepts, c)
				}
			}
		}
	}

	// 2. 经营范围关键词映射
	if resp.Jbzl.Jyfw != "" {
		for keyword, list := range scopeConceptMap {
			if strings.Contains(resp.Jbzl.Jyfw, keyword) {
				for _, c := range list {
					if _, exists := seen[c]; !exists {
						seen[c] = struct{}{}
						concepts = append(concepts, c)
					}
				}
			}
		}
	}

	// 3. 证监会行业补充（若未匹配到）
	if len(concepts) == 0 && resp.Jbzl.Sszjhy != "" {
		parts := strings.Split(resp.Jbzl.Sszjhy, "-")
		if len(parts) > 1 {
			concepts = append(concepts, parts[1])
		} else {
			concepts = append(concepts, resp.Jbzl.Sszjhy)
		}
	}

	// 风口标签
	wind := calcWindLabel(changePercent, resp.Jbzl.Sshy)

	return &StockConcepts{Concepts: concepts, Wind: wind}, nil
}

func calcWindLabel(changePercent float64, industry string) string {
	hotKeywords := []string{"AI", "芯片", "半导体", "新能源", "机器人", "低空经济", "商业航天", "算力", "固态电池", "光伏", "创新药", "黄金", "消费电子", "智能驾驶", "军工"}
	isHotIndustry := false
	for _, kw := range hotKeywords {
		if strings.Contains(industry, kw) {
			isHotIndustry = true
			break
		}
	}

	if changePercent > 5 {
		if isHotIndustry {
			return "🔥 强势风口"
		}
		return "📈 强势上涨"
	}
	if changePercent > 2 {
		if isHotIndustry {
			return "🔥 活跃风口"
		}
		return "📈 活跃"
	}
	if changePercent >= 0 {
		if isHotIndustry {
			return "🔥 热门赛道"
		}
		return "➡️ 平稳"
	}
	if changePercent > -2 {
		return "➡️ 小幅调整"
	}
	return "❄️ 调整"
}

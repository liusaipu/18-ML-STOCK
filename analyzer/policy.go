// BuildPolicyMatch 根据行业和概念生成十五五政策匹配评估
func BuildPolicyMatch(industry string, concepts []string) *PolicyMatchData {
	pm := &PolicyMatchData{
		Industry: industry,
		Score:    0,
		Policies: []string{},
	}
	seen := make(map[string]struct{})

	// 1. 行业映射（权重最高）
	if list, ok := industryPolicyMap[industry]; ok {
		for _, p := range list {
			if _, exists := seen[p]; !exists {
				seen[p] = struct{}{}
				pm.Policies = append(pm.Policies, p)
			}
		}
	}

	// 2. 概念补充映射
	for _, c := range concepts {
		for keyword, list := range conceptPolicyMap {
			if strings.Contains(c, keyword) {
				for _, p := range list {
					if _, exists := seen[p]; !exists {
						seen[p] = struct{}{}
						pm.Policies = append(pm.Policies, p)
					}
				}
			}
		}
	}

	// 3. 计算评分与评级
	if len(pm.Policies) >= 4 {
		pm.Score = 85 + min(15, len(pm.Policies)*2)
		pm.MatchLevel = "强匹配"
	} else if len(pm.Policies) >= 2 {
		pm.Score = 55 + min(29, len(pm.Policies)*10)
		pm.MatchLevel = "中性"
	} else if len(pm.Policies) == 1 {
		pm.Score = 50
		pm.MatchLevel = "中性"
	} else {
		pm.Score = 35
		pm.MatchLevel = "弱匹配"
		pm.Policies = []string{"传统领域", "转型观望"}
	}

	// 4. 生成解读摘要
	pm.Summary = buildPolicySummary(pm)
	return pm
}

func buildPolicySummary(pm *PolicyMatchData) string {
	switch pm.MatchLevel {
	case "强匹配":
		return "该企业所处行业及业务方向与国家重点产业政策高度契合，未来有望持续享受政策红利与资金支持。"
	case "中性":
		return "该企业部分业务与产业政策存在一定交集，可享受适度的政策支持，但红利弹性相对有限。"
	default:
		return "该企业所处领域当前并非国家重点发力方向，政策红利不明显，需更多依靠自身内生增长。"
	}
}

func min(a, b int) int {
	if a < b {
		return a
	}
	return b
}
